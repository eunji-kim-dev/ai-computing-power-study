`timescale 1ns / 1ps

module tb_rca_top;

    reg  [15:0] sw;
    wire [15:0] led;

    rca_top uut (
        .sw(sw),
        .led(led)
    );

    initial begin
        // 초기값
        sw = 16'b0000_0000_0000_0000;
        #10;

        // A = 0000, B = 0000, Cin = 0
        // 결과: Sum = 0000, Cout = 0
        sw = 16'b0000_0000_0000_0000;
        #10;

        // A = 0001, B = 0001, Cin = 0
        // 결과: 1 + 1 = 2
        // Sum = 0010, Cout = 0
        sw = 16'b0000_0000_0001_0001;
        #10;

        // A = 0011, B = 0101, Cin = 0
        // 결과: 3 + 5 = 8
        // Sum = 1000, Cout = 0
        sw = 16'b0000_0000_0101_0011;
        #10;

        // A = 0111, B = 0001, Cin = 0
        // 결과: 7 + 1 = 8
        // Sum = 1000, Cout = 0
        sw = 16'b0000_0000_0001_0111;
        #10;

        // A = 1111, B = 0001, Cin = 0
        // 결과: 15 + 1 = 16
        // Sum = 0000, Cout = 1
        sw = 16'b0000_0000_0001_1111;
        #10;

        // A = 1111, B = 1111, Cin = 0
        // 결과: 15 + 15 = 30
        // Sum = 1110, Cout = 1
        sw = 16'b0000_0000_1111_1111;
        #10;

        // A = 1111, B = 1111, Cin = 1
        // sw[8] = 1
        // 결과: 15 + 15 + 1 = 31
        // Sum = 1111, Cout = 1
        sw = 16'b0000_0001_1111_1111;
        #10;

        // A = 1010, B = 0101, Cin = 1
        // 결과: 10 + 5 + 1 = 16
        // Sum = 0000, Cout = 1
        sw = 16'b0000_0001_0101_1010;
        #10;

        $stop;
    end

endmodule